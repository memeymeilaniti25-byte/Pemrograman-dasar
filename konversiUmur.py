umur = int(input("Masukkan umur nanas dalam tahun:"))
satu_tahun_dalam_bulan = 12
satu_tahun_dalam_minggu = 52
satu_tahun_dalam_hari = 365

umur_dalam_bulan = umur * satu_tahun_dalam_bulan
umur_dalam_minggu = umur * satu_tahun_dalam_minggu
umur_dalam_hari = umur * satu_tahun_dalam_hari


print (f"Umur nanas dalam bulan: {umur_dalam_bulan} bulan")
print (f"Umur nanas dalam minggu: {umur_dalam_minggu} minggu")
print (f"Umur nanas dalam hari: {umur_dalam_hari} hari")